    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        String nome = "";
        String senha = "";
        Integer id = null;
        String cpf = DeleteCpf.getText();

        String sql = "DELETE FROM usuario WHERE CPF = ?";
        try (Connection conn = ConexaoBD.obterConexao(); PreparedStatement ps = conn.prepareStatement(sql)) {

            Usuario usuario = new Usuario(nome, senha, id, cpf);
            DAO dao = new DAO();

            if (dao.existeCpf(usuario)) {

                ps.setString(1, cpf);
                int k = ps.executeUpdate();

                if (k == 1) {
                    JOptionPane.showMessageDialog(this, "Cadastro Deletado");
                    
                } else {
                    JOptionPane.showMessageDialog(this, "Erro");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Cadastro n√£o localizado");
            }

        } catch (Exception ex) {
            Logger.getLogger(AlterarCadastro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

  

